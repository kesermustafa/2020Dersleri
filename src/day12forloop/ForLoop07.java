package day12forloop;

public class ForLoop07 {

	public static void main(String[] args) {
		/*
		 3 den 6 ya kadar tum tam sayilarin carpimini hesaplayan pragram yazin
		 */

		int carpim = 1;
		
		for(int i=3; i<=5 ; i++) {
			carpim = carpim * i;
		}
		System.out.println("carpim : " + carpim);
				
		
		
		
		
	}

}
